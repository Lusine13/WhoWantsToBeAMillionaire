const generateFibonacci = (n) => {
    const fib = [];
    for (let i = 0; i < n; i++) {
        if (i === 0) {
            fib.push(1000); 
        } else if (i === 1) {
            fib.push(2000); 
        } else {
            fib.push(fib[i - 1] + fib[i - 2]); 
        }
    }
    return fib;
      
    
  };
  
